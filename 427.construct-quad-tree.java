class Solution {
    public boolean sameValue(int x, int y, int length, int[][] grid) {
        int std = grid[x][y];
        for (int i = x ; i < x + length; i++) {
            for (int j = y; j < y + length; j++) {
                if (grid[i][j] != std) return false;
            }
        }
        return true;
    }

    public Node helper(int[][] grid, int x, int y, int length) {
        if (sameValue(x, y, length, grid)) {
            boolean val = grid[x][y] == 1 ? true : false;
            return new Node(val, true);
        }
        Node root = new Node(false, false);
        root.topLeft = helper(grid, x, y, length / 2);
        root.topRight = helper(grid, x, y + length / 2, length / 2);
        root.bottomLeft = helper(grid, x + length / 2, y, length / 2);
        root.bottomRight = helper(grid, x + length / 2, y + length / 2, length / 2);
        return root;
    }
    
    public Node construct(int[][] grid) {
        int n = grid.length;
        return helper(grid, 0, 0, n);
    }
}
